
package kg.geektech.weatherapp.data.models.five_days;

public class Coord {

    private Double lat;
    private Double lon;

    public Coord() {
    }

    public Double getLat() {
        return lat;
    }

    public void setLat(Double lat) {
        this.lat = lat;
    }

    public Double getLon() {
        return lon;
    }

    public void setLon(Double lon) {
        this.lon = lon;
    }

}
